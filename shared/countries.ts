// Helper functions
export function getCountryByCode(code: string) {
  return countries.find(country => country.code === code);
}

export function getRegionByCountryCode(code: string) {
  for (const [region, countryCodes] of Object.entries(regions)) {
    if (countryCodes.includes(code)) {
      return region;
    }
  }
  return "Other";
}

export function getCountriesByRegion(region: string) {
  const regionCodes = regions[region as keyof typeof regions] || [];
  return countries.filter(country => regionCodes.includes(country.code));
}
